package Combos;

import java.util.List;

/*
 *
 * @author  cabrerar
 */
public class Entidad {

    private String strCodEnt;
    private String strDescription;
    private List lstMunicipio = null;
    private int clPais;

    /* Creates a new instance of Entidad */
    public Entidad() {
    }

    public String getStrCodEnt() {
        return this.strCodEnt;
    }

    public void setStrCodEnt(String strCodEnt) {
        this.strCodEnt = strCodEnt;
    }

    public String getStrDescription() {
        return this.strDescription;
    }

    public void setStrDescription(String strDescription) {
        this.strDescription = strDescription;
    }

    public List getLstMunicipio() {
        return this.lstMunicipio;
    }

    public void setLstMunicipio(List lstMunicipio) {
        this.lstMunicipio = lstMunicipio;
    }

    public int getClPais() {
        return clPais;
    }

    public void setClPais(int clPais) {
        this.clPais = clPais;
    }
}
