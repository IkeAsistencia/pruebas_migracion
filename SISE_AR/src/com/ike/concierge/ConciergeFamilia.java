package com.ike.concierge;

public class ConciergeFamilia {

    private String clEvento;
    private String clConcierge;
    private String clGenero;
    private String dsGenero;
    private String clParentesco;
    private String dsParentesco;
    private String NombreFam;
    private String FechaNac;
    private String clCompFam;
    

    public ConciergeFamilia() {
    }

    public String getClEvento() {
        return clEvento;
    }

    public void setClEvento(String clEvento) {
        this.clEvento = clEvento;
    }

    public String getClConcierge() {
        return clConcierge;
    }

    public void setClConcierge(String clConcierge) {
        this.clConcierge = clConcierge;
    }

    public String getClGenero() {
        return clGenero;
    }

    public void setClGenero(String clGenero) {
        this.clGenero = clGenero;
    }
    
    public String getDsGenero() {
        return dsGenero;
    }

    public void setDsGenero(String dsGenero) {
        this.dsGenero = dsGenero;
    }

    public String getClParentesco() {
        return clParentesco;
    }

    public void setClParentesco(String clParentesco) {
        this.clParentesco = clParentesco;
    }
    
    public String getDsParentesco() {
        return dsParentesco;
    }

    public void setDsParentesco(String dsParentesco) {
        this.dsParentesco = dsParentesco;
    }

    public String getNombreFam() {
        return NombreFam;
    }

    public void setNombreFam(String NombreFam) {
        this.NombreFam = NombreFam;
    }

    public String getFechaNac() {
        return FechaNac;
    }

    public void setFechaNac(String FechaNac) {
        this.FechaNac = FechaNac;
    }

    public String getClCompFam() {
        return clCompFam;
    }

    public void setClCompFam(String clCompFam) {
        this.clCompFam = clCompFam;
    }    

}
