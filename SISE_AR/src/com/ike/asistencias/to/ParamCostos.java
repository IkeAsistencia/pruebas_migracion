public class ParamCostos {    
    
    public ParamCostos() {
    }


    private String id;
    private String GestionCat;
    private String PorcentajeRec;
    private String Usuario;
    private String fechaCambio;
    

    
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
    public String getGestionCat() {
        return GestionCat;
    }

    public void setGestionCat(String GestionCat) {
        this.GestionCat = GestionCat;
    }

    public String getPorcentajeRec() {
        return PorcentajeRec;
    }

    public void setPorcentajeRec(String PorcentajeRec) {
        this.PorcentajeRec = PorcentajeRec;
    }

    public String getUsuario() {
        return Usuario;
    }

    public void setUsuario(String Usuario) {
        this.Usuario = Usuario;
    }

    public String getFechaCambio() {
        return fechaCambio;
    }

    public void setFechaCambio(String fechaCambio) {
        this.fechaCambio = fechaCambio;
    }
         

         
         
         
    
    
}
